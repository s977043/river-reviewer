#!/usr/bin/env node
/**
 * Generate a Markdown audit report from evaluate-all JSON output.
 * Usage: node scripts/generate-audit-report.mjs <eval-output.json>
 */
import fs from 'node:fs';
import path from 'node:path';
import url from 'node:url';

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const LEDGER_PATH = path.join(ROOT, 'artifacts', 'evals', 'results.jsonl');
const OUTPUT_PATH = 'audit-report.md';
const DEGRADATION_THRESHOLD = 0.05;

const RATIO_METRICS = new Set([
  'exactMatch', 'top1Match', 'coverage', 'mrr',
  'passRate', 'falsePositiveRate', 'evidenceRate', 'verifierRejectRate',
  'policyPassRate', 'memoryRecallRate', 'escalationAccuracy',
  'suppressionAccuracy', 'resurfaceAccuracy',
]);

export function formatMetric(key, value) {
  if (typeof value !== 'number') return String(value ?? '-');
  const shortKey = key.replace(/^[a-z]+_/, '');
  return RATIO_METRICS.has(shortKey) ? (value * 100).toFixed(1) + '%' : String(value);
}

export function computeTrend(current, previous) {
  if (!previous?.scores || !current?.scores) return [];
  const trends = [];
  for (const [key, val] of Object.entries(current.scores)) {
    if (typeof val !== 'number') continue;
    const prev = previous.scores[key];
    if (typeof prev !== 'number') continue;
    const delta = val - prev;
    const degraded = delta < -DEGRADATION_THRESHOLD;
    trends.push({ key, current: val, previous: prev, delta, degraded });
  }
  return trends;
}

function loadPreviousEntry() {
  try {
    const raw = fs.readFileSync(LEDGER_PATH, 'utf8').trim();
    if (!raw) return null;
    const lines = raw.split('\n').filter(Boolean);
    if (lines.length < 2) return null;
    return JSON.parse(lines[lines.length - 2]);
  } catch {
    return null;
  }
}

export function generateReport(envelope, previousEntry) {
  const lines = [];
  lines.push('# Nightly Audit Report\n');
  lines.push('- **Timestamp**: ' + envelope.timestamp);
  lines.push('- **Commit**: ' + envelope.commit);
  lines.push('- **Branch**: ' + envelope.branch);
  lines.push('- **Status**: ' + (envelope.status?.toUpperCase() ?? 'UNKNOWN') + '\n');

  lines.push('## Sub-eval Results\n');
  lines.push('| Name | Status | Key Metrics |');
  lines.push('|------|--------|-------------|');
  for (const r of (envelope.results ?? [])) {
    const status = r.pass ? '✅ PASS' : '❌ FAIL';
    const metricKeys = Object.keys(envelope.scores ?? {}).filter(k => k.startsWith(r.name + '_'));
    const metricStr = metricKeys.map(k => {
      const shortKey = k.replace(r.name + '_', '');
      return shortKey + ': ' + formatMetric(shortKey, envelope.scores[k]);
    }).join(', ') || '-';
    lines.push('| ' + r.name + ' | ' + status + ' | ' + metricStr + ' |');
  }
  lines.push('');

  lines.push('## Trend Comparison\n');
  if (!previousEntry) {
    lines.push('_No previous data available for comparison._\n');
  } else {
    const trends = computeTrend(envelope, previousEntry);
    if (!trends.length) {
      lines.push('_No comparable metrics found._\n');
    } else {
      lines.push('| Metric | Current | Previous | Delta | Alert |');
      lines.push('|--------|---------|----------|-------|-------|');
      for (const t of trends) {
        const alert = t.degraded ? '⚠️ DEGRADED' : '';
        lines.push('| ' + t.key + ' | ' + formatMetric(t.key, t.current) + ' | ' + formatMetric(t.key, t.previous) + ' | ' + (t.delta >= 0 ? '+' : '') + formatMetric(t.key, t.delta) + ' | ' + alert + ' |');
      }
      lines.push('');
      const degraded = trends.filter(t => t.degraded);
      if (degraded.length) {
        lines.push('### ⚠️ Degradation Alert\n');
        lines.push(degraded.length + ' metric(s) degraded by more than ' + (DEGRADATION_THRESHOLD * 100) + '%:\n');
        for (const d of degraded) {
          lines.push('- **' + d.key + '**: ' + formatMetric(d.key, d.previous) + ' → ' + formatMetric(d.key, d.current));
        }
        lines.push('');
      }
    }
  }

  lines.push('---');
  lines.push('_Generated by river-reviewer nightly audit_');
  return lines.join('\n');
}

async function main() {
  const inputPath = process.argv[2];
  if (!inputPath) {
    console.error('Usage: node scripts/generate-audit-report.mjs <eval-output.json>');
    process.exit(1);
  }

  let envelope;
  try {
    envelope = JSON.parse(fs.readFileSync(inputPath, 'utf8'));
  } catch (err) {
    console.error('Failed to read eval output: ' + err.message);
    process.exit(1);
  }

  const previousEntry = loadPreviousEntry();
  const report = generateReport(envelope, previousEntry);
  fs.writeFileSync(OUTPUT_PATH, report);
  console.log('Audit report written to ' + OUTPUT_PATH);
}

const isDirectRun = process.argv[1] &&
  (process.argv[1].endsWith('generate-audit-report.mjs') || process.argv[1].endsWith('generate-audit-report'));

if (isDirectRun) {
  main().catch(err => { console.error('Audit report error: ' + err.message); process.exitCode = 1; });
}
