#!/usr/bin/env node
/**
 * Compare the latest entries of artifacts/evals/results.jsonl and emit a
 * delta table (markdown). Used to surface cross-run KPI drift when running
 * `evaluate-all.mjs --append-ledger` multiple times locally, or when
 * downloading the persisted artifact from nightly-eval.
 *
 * Usage:
 *   node scripts/compare-eval-ledger.mjs                    # compare last 2 entries
 *   node scripts/compare-eval-ledger.mjs --last 3           # compare last 3 entries
 *   node scripts/compare-eval-ledger.mjs --ledger <path>    # custom ledger path
 *   node scripts/compare-eval-ledger.mjs --json             # JSON output
 *
 * Exit codes:
 *   0 — comparison emitted (or insufficient entries, no comparison possible)
 *   1 — KPI regression detected (latest entry has any score lower than the previous)
 *   2 — fatal error (file missing / parse failure)
 */
import fs from 'node:fs/promises';
import path from 'node:path';
import url from 'node:url';

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const DEFAULT_LEDGER = path.join(ROOT, 'artifacts', 'evals', 'results.jsonl');

function parseArgs(argv) {
  const args = { last: 2, ledger: DEFAULT_LEDGER, json: false, help: false };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--help' || a === '-h') args.help = true;
    else if (a === '--last') args.last = Number(argv[++i] ?? 2);
    else if (a === '--ledger') args.ledger = argv[++i] ?? DEFAULT_LEDGER;
    else if (a === '--json') args.json = true;
  }
  return args;
}

function printHelp() {
  process.stdout.write(`Usage: node scripts/compare-eval-ledger.mjs [options]

Options:
  --last <N>          Compare the last N entries (default: 2)
  --ledger <path>     Ledger file path (default: artifacts/evals/results.jsonl)
  --json              Output JSON instead of markdown
  -h, --help          Show this help

Exit codes:
  0  comparison emitted (or insufficient entries)
  1  KPI regression detected
  2  fatal error
`);
}

async function readLedger(p) {
  const text = await fs.readFile(p, 'utf8');
  const entries = [];
  for (const line of text.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed) continue;
    try {
      entries.push(JSON.parse(trimmed));
    } catch (err) {
      throw new Error(
        `failed to parse ledger line: ${err.message}\n  line: ${trimmed.slice(0, 200)}`
      );
    }
  }
  return entries;
}

function computeDelta(prev, curr) {
  const prevScores = prev.scores ?? {};
  const currScores = curr.scores ?? {};
  const keys = new Set([...Object.keys(prevScores), ...Object.keys(currScores)]);
  const rows = [];
  let regression = false;
  for (const k of [...keys].sort()) {
    const a = prevScores[k];
    const b = currScores[k];
    const aNum = typeof a === 'number';
    const bNum = typeof b === 'number';
    const delta = aNum && bNum ? b - a : null;
    // Most KPIs are "higher is better"; falsePositiveRate / errorCount /
    // failure-count metrics generated by evaluate-all.mjs (`fail_<category>`)
    // are "lower is better".
    const lowerIsBetter = /falsePositiveRate$|errorCount$|(?:^|_)fail_/.test(k);
    const isRegression = delta !== null && (lowerIsBetter ? delta > 0.001 : delta < -0.001);
    if (isRegression) regression = true;
    rows.push({ key: k, prev: a, curr: b, delta, regression: isRegression, lowerIsBetter });
  }
  return { rows, regression };
}

function formatNumber(v) {
  if (typeof v !== 'number') return v == null ? '-' : String(v);
  if (v === 0) return '0';
  if (Math.abs(v) < 0.0001) return v.toExponential(2);
  return v.toFixed(4).replace(/\.?0+$/, '');
}

function renderMarkdown(prev, curr, delta) {
  const lines = [];
  lines.push('# KPI Ledger Comparison');
  lines.push('');
  lines.push(
    `**Previous**: ${prev.timestamp ?? '?'} (${prev.commit?.slice(0, 7) ?? '?'} on ${prev.branch ?? '?'})`
  );
  lines.push(
    `**Latest**:   ${curr.timestamp ?? '?'} (${curr.commit?.slice(0, 7) ?? '?'} on ${curr.branch ?? '?'})`
  );
  lines.push('');
  lines.push('| Metric | Prev | Latest | Δ | Note |');
  lines.push('| ------ | ---- | ------ | - | ---- |');
  for (const r of delta.rows) {
    const note = r.regression
      ? '⚠ regression'
      : r.delta && Math.abs(r.delta) > 0.001
        ? '✓ improved or moved'
        : '';
    const direction = r.lowerIsBetter ? '↓ better' : '↑ better';
    const noteFull = r.regression ? `⚠ regression (${direction})` : note;
    lines.push(
      `| ${r.key} | ${formatNumber(r.prev)} | ${formatNumber(r.curr)} | ${
        r.delta == null ? '-' : (r.delta >= 0 ? '+' : '') + formatNumber(r.delta)
      } | ${noteFull} |`
    );
  }
  lines.push('');
  if (delta.regression) {
    lines.push('**Status**: ⚠ One or more KPIs regressed.');
  } else {
    lines.push('**Status**: ✓ No regression detected.');
  }
  return lines.join('\n');
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.help) {
    printHelp();
    return 0;
  }
  let entries;
  try {
    entries = await readLedger(args.ledger);
  } catch (err) {
    if (err.code === 'ENOENT') {
      process.stderr.write(`ledger not found: ${args.ledger}\n`);
      return 2;
    }
    process.stderr.write(`failed to read ledger: ${err.message}\n`);
    return 2;
  }
  if (entries.length < 2) {
    const msg = entries.length === 0 ? 'no entries' : 'only 1 entry';
    process.stdout.write(
      args.json
        ? JSON.stringify({ status: 'insufficient', message: msg, entryCount: entries.length }) +
            '\n'
        : `Insufficient ledger entries (${msg}). Need at least 2 to compare.\n`
    );
    return 0;
  }
  const lastN = Math.max(2, args.last);
  const window = entries.slice(-lastN);
  // Compare the OLDEST and NEWEST entries within the requested window so that
  // `--last N` yields a "current vs N-ago" delta. With the default N=2 this
  // reduces to "previous vs latest" which is the most common usage.
  const prev = window[0];
  const curr = window[window.length - 1];
  const delta = computeDelta(prev, curr);

  if (args.json) {
    process.stdout.write(
      JSON.stringify({
        previous: prev,
        latest: curr,
        delta: delta.rows,
        regression: delta.regression,
      }) + '\n'
    );
  } else {
    process.stdout.write(renderMarkdown(prev, curr, delta) + '\n');
  }
  return delta.regression ? 1 : 0;
}

main().then(
  (code) => process.exit(code),
  (err) => {
    process.stderr.write(`fatal: ${err.message}\n`);
    process.exit(2);
  }
);
